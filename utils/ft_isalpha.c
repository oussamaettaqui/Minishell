#include "../minishell.h"

int	ft_isalpha(int c)
{
	if (c == 42 || c == 95 || (c >= 48 && c <= 57) || (c >= 65 && c <= 90)
		|| (c >= 97 && c <= 122))
	{
		return (1);
	}
	return (0);
}
